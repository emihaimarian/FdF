#include <mlx_math.h>

t_mx2	mx_mult_2x2(t_mx2 m1, t_mx2 m2)
{
	t_mx2	result;
	int		i;

	i = 0;
	while (i < 2)
	{
		result.x[i] = m1.x[i] * m2.x[0] + m1.y[i] * m2.x[1];
		result.y[i] = m1.x[i] * m2.y[0] + m1.y[i] * m2.y[1];
		i++;
	}
	return (result);
}
